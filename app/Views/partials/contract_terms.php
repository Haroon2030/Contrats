function renderContractTermsHtml(string $text): string
{
    $lines = preg_split('/\R/u', $text);
    $html = '';
    $openedClause = false;
    $inDefinitions = false;
    $definitionRows = [];

    $flushDefinitions = function() use (&$html, &$definitionRows) {
        if (empty($definitionRows)) {
            return;
        }

        $html .= '<table class="terms-def-table"><thead><tr><th>البند</th><th>التعريف</th></tr></thead><tbody>';

        foreach ($definitionRows as $row) {
            $html .= '<tr><td>' . e($row[0]) . '</td><td>' . e($row[1]) . '</td></tr>';
        }

        $html .= '</tbody></table>';
        $definitionRows = [];
    };

    $closeClause = function() use (&$html, &$openedClause) {
        if ($openedClause) {
            $html .= '</div>';
            $openedClause = false;
        }
    };

    foreach ($lines as $rawLine) {
        $line = trim($rawLine);

        if ($line === '') {
            continue;
        }

        $line = preg_replace('/\s+/u', ' ', $line);

        if ($line === 'الشروط والأحكام') {
            $flushDefinitions();
            $closeClause();
            $inDefinitions = false;
            $html .= '<h2 class="terms-main-heading">الشروط والأحكام</h2>';
            continue;
        }

        if ($line === 'تعريفات البنود الواردة في العقد') {
            $flushDefinitions();
            $closeClause();
            $inDefinitions = true;
            $html .= '<h2 class="terms-main-heading">تعريفات البنود الواردة في العقد</h2>';
            continue;
        }

        if (mb_strpos($line, 'أحكام عامة') === 0) {
            $flushDefinitions();
            $closeClause();
            $inDefinitions = false;
            $html .= '<h2 class="terms-main-heading">أحكام عامة</h2>';

            $rest = trim(str_replace('أحكام عامة', '', $line));
            if ($rest !== '') {
                $html .= '<p class="terms-paragraph">' . e($rest) . '</p>';
            }
            continue;
        }

        if ($inDefinitions) {
            $parts = preg_split('/\t+| (2,)/u', $line, 2);

            if (count($parts) === 2) {
                $definitionRows[] = [$parts[0], $parts[1]];
            } else {
                if (!empty($definitionRows)) {
                    $last = count($definitionRows) - 1;
                    $definitionRows[$last][1] .= ' ' . $line;
                } else {
                    $html .= '<p class="terms-paragraph">' . e($line) . '</p>';
                }
            }
            continue;
        }

        if (preg_match('/^(\d+)\)\s*(.*)$/u', $line, $m)) {
            $flushDefinitions();
            $closeClause();

            $num = $m[1];
            $title = trim($m[2]);

            $html .= '<div class="terms-clause">';
            $html .= '<div class="terms-clause-head"><span class="terms-clause-num">' . e($num) . '</span><span>' . e($title !== '' ? $title : 'بند') . '</span></div>';
            $openedClause = true;
            continue;
        }

        if (preg_match('/^[•\-]\s*(.*)$/u', $line, $m)) {
            $html .= '<div class="terms-bullet">• ' . e($m[1]) . '</div>';
            continue;
        }

        if (preg_match('/^\d+\-\s*(.*)$/u', $line, $m)) {
            $html .= '<div class="terms-subitem">' . e($line) . '</div>';
            continue;
        }

        if (!$openedClause) {
            $html .= '<p class="terms-preamble">' . e($line) . '</p>';
        } else {
            $html .= '<p class="terms-paragraph">' . e($line) . '</p>';
        }
    }

    $flushDefinitions();
    $closeClause();

    return $html;
}
